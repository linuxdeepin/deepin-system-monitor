#ifndef HASHQSTRING_H
#define HASHQSTRING_H

#include <QString>
#include <string>

#if QT_VERSION < QT_VERSION_CHECK(5, 14, 0)
namespace std {
    template<> struct hash<QString>
    {
        size_t operator()(const QString& k) const
            {
                return hash<string>()(k.toStdString());
            }
    };
}
#endif

#endif // HASHQSTRING_H
